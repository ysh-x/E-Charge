    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        // TODO add your handling code here:
        WalletID = IDText.getText().toUpperCase();
        Payment = PaymentText.getText();
        Amount = Float.parseFloat(AmountText.getText());
        
        try {
            String url = "jdbc:h2:~/test";
            Connection C = DriverManager.getConnection(url,"sa","sa");
             
            
            String query = "UPDATE CHARGEWALLET SET BALANCE = BALANCE + "+Amount+"WHERE ID = '"+WalletID+"';";
      
            Statement S = C.createStatement();
            S.executeUpdate(query);
            
           showMessageDialog(this,"Successfully Updated The Wallet!","ALERT",JOptionPane.INFORMATION_MESSAGE);
                    
            C.close();
            S.close();
        } catch (SQLException E) {System.out.println(E);
         showMessageDialog(this,"\t\tError Occured","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_UpdateBtnMouseClicked

    private void BalanceBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BalanceBtnMouseClicked
        // TODO add your handling code here:
        try {
            WalletID = IDText.getText().toUpperCase();
             Payment = PaymentText.getText();
            
            
            String url = "jdbc:h2:~/test";
            Connection C = DriverManager.getConnection(url,"sa","sa");
       
         
            String query = "Select count(*),BALANCE from CHARGEWALLET where ID = '"+WalletID+"';";
          
            
            Statement S = C.createStatement(); 
            System.out.println(query);
            ResultSet RS = S.executeQuery(query);
            
            while(RS.next()) {
            Float tempAmt = RS.getFloat("BALANCE");
            String strAmt = tempAmt.toString();
            if(RS.getInt("count(*)") != 0 ) {
                    BalanceHolder.setText(strAmt);
                }
            else {
                    showMessageDialog(this,"\t\t Wallet ID Does Not Exist","Balance Load Error (01)",JOptionPane.ERROR_MESSAGE);

            }
            }
           S.close();
           C.close();
        } catch (SQLException E) {
            System.out.println(E); 
             showMessageDialog(this,"\t\tError Occured","ERROR",JOptionPane.ERROR_MESSAGE);
            
        }  
        
    }//GEN-LAST:event_BalanceBtnMouseClicked

